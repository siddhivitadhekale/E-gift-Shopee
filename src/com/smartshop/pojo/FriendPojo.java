package com.smartshop.pojo;

public class FriendPojo {
	
	int id;
	
	String from_friend;
	String to_friend;
	String date;
	String status;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getFrom_friend() {
		return from_friend;
	}
	public void setFrom_friend(String from_friend) {
		this.from_friend = from_friend;
	}
	public String getTo_friend() {
		return to_friend;
	}
	public void setTo_friend(String to_friend) {
		this.to_friend = to_friend;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
	
	
	
	
}
