package com.smartshop.pojo;

public class TransctionPojo {
	
	int transction_id;
	int total;
	
	String user_name;
	String to_friend;
	String date;
	public int getTransction_id() {
		return transction_id;
	}
	public void setTransction_id(int transction_id) {
		this.transction_id = transction_id;
	}
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}
	public String getUser_name() {
		return user_name;
	}
	public void setUser_name(String user_name) {
		this.user_name = user_name;
	}
	public String getTo_friend() {
		return to_friend;
	}
	public void setTo_friend(String to_friend) {
		this.to_friend = to_friend;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	
	
	
	
	
	
}
